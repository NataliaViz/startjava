public class VariableNamesTheme {
    public static void main(String[] args) {
        System.out.println("1. Разные переменные");

        // цифра 
        int number = 5;
        System.out.println("цифра = " + number);

        // сумма чисел
        int sum = 5 + 6;
        System.out.println("сумма чисел = " + sum);

        // произведенеи цифр
        int product = 5 * 2;
        System.out.println("произведение цифр = " + product);

        //максимальное число
        int maxNumber = 2147483647;
        System.out.println("максимальное число = " + maxNumber);

        //количество десятков
        int tens = 2147483647 / 10;
        System.out.println("количество десятков = " + tens);

        //вес собаки
        int dogWeight = 25;
        System.out.println("вес собаки = " + dogWeight);

        //исходное число
        int originalNumber = 5;
        System.out.println("исходное число = " + originalNumber);

        //процент по вкладу
        int deposit = 30;
        System.out.println("процент по вкладу = " + deposit);

        //с символом &
        char symbolAnd = '&';
        System.out.println("переменная с символом = " + symbolAnd);

        //код ошибки
        int errorCode = 400;
        System.out.println("код ошибки = " + errorCode);

        //
    }
}